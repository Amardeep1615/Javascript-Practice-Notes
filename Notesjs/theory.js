//.................................................Scope of Variables ...........................................................................................................
// They are 3 types of scope in JavaScript
//1. Global Scope : Variables declared outside any function/block. Accessible everywhere.
//2. Function Scope,Local Scope:Variables declared inside a function. Accessible only within that function.
//3. Block Scope :Variables declared inside a block of code like if,else,switch,for,while,try..

//***var – Function Scoped***
//var is function-scoped and hoisted (moved to the top of its scope). It doesn't respect block scope (like if, for blocks).

//***let – Block Scoped***
//let is block-scoped and not hoisted in a usable way. It’s the preferred way to declare mutable variables.

//***const – Block Scoped, Constant***
//const is block-scoped like let, but you cannot reassign a new value to it after initialization.

